import Head from 'next/head'
import React from 'react'
import useSWR from 'swr'
import { WeightEntity } from '@/entities'
import { WeightRepository } from '@/repositories'
import styles from '@/styles/Home.module.css'

type Props = {
  weights: Array<WeightEntity>
}

export async function getServerSideProps() {
  try {
    const weights = await WeightRepository.fetchWeights()

    return {
      props: {
        weights
      }
    }
  } catch (error) {
    console.log(error)
  } 
}

export default function Home(props: Props): JSX.Element {
  return (
    <div className={styles.container}>
      <Head>
        <title>My Wight Checker</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className={styles.main}>
        <h1 className={styles.title}>
          Welcome to <a href="https://nextjs.org">{props.weights[0].weight}</a>
        </h1>
      </main>

      <footer className={styles.footer}>
        <p>Powered by Takaki Miyajima</p>
      </footer>
    </div>
  )
}
