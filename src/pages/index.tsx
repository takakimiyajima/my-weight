import Head from 'next/head'
import React from 'react'
import { WeightEntity } from '@/entities'
import { WeightRepository } from '@/repositories'
import styles from '@/styles/Home.module.css'

type Props = {
  weights: Array<WeightEntity>
}

export async function getServerSideProps() {
  try {
    const weights = await WeightRepository.fetchWeights()

    return {
      props: {
        weights
      }
    }
  } catch (error) {
    console.log(error)
  } 
}

export default function Home(props: Props) {
  // TODO: prepare some components
  return (
    <div className={styles.container}>
      <Head>
        <title>My Wight Checker</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className={styles.main}>
        <h1 className={styles.title}>How is your goal??</h1>
      </main>

      {props.weights.map((weight, index) => (
        <div key={index}>
          <p>{weight.weight}</p>
          <p>{weight.workOutDate}</p>
        </div>
      ))}

      <footer className={styles.footer}>
        <p>Powered by Takaki Miyajima</p>
      </footer>
    </div>
  )
}
